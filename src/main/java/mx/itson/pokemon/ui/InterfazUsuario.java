    private void buttonArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonArchivoActionPerformed
        try{
           JFileChooser fileChooser = new JFileChooser();
           fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
           
           if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
               File file = fileChooser.getSelectedFile();
               
               byte fileBytes[] = Files.readAllBytes(file.toPath());
               String contenido = new String(fileBytes, StandardCharsets.UTF_8);
               
               
                Recibo recibo = new Recibo().deserializar(contenido);
                
                labelGerente.setText("Gerente: " + recibo.getAtendidoPor());
                labelNombreCliente.setText("Nombre: " + recibo.getCliente().getNombre());
                labelRfc.setText("RFC: " + recibo.getCliente().getRfc());
                labelDireccion.setText ("Direccion: " + recibo.getCliente().getDireccion());
                
                
                
                DefaultTableModel model = (DefaultTableModel) tablaArticulos.getModel();
                model.setRowCount(0);
                double totalFinal = 0;
                for(Articulo c : recibo.getArticulos()){
                    double totalIva = (calculadora.iva(c.getPrecio(), c.getTasaIva()) + c.getPrecio());
                    totalFinal += totalIva;
                    model.addRow(new Object[] {
                        c.getCodigo(),
                        c.getNombre(),
                        c.getPrecio(),
                        c.getTasaIva(),
                        totalIva
                    });
                    
                    labelFinal.setText("TOTAL = " + totalFinal);
                }
                
           }
       }catch (Exception ex){
           System.err.println(ex.getMessage());
       }
    }//GEN-LAST:event_buttonArchivoActionPerformed

